package com.nt.mod1;

public class ArithmeticOperation {

	
	public int sum(int x , int y) {
		return x+y;
	}
	public int sub(int x , int y) {
		return x-y;
	}
	public int equal(int x , int y) {
		if (x==y)
		return 1;
		else
			return 0;
	}
	public int mul(int x , int y) {
		return x*y;
	
}
}
